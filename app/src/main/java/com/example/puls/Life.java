package com.example.puls;

public class Life {
    double Weight;
    int Step;

    public Life(double weight, int step) {
        Weight = weight;
        Step = step;
    }

    public double getWeight() {
        return Weight;
    }

    public void setWeight(double weight) {
        Weight = weight;
    }

    public int getStep() {
        return Step;
    }

    public void setStep(int step) {
        Step = step;
    }
}
