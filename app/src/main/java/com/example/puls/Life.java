package com.example.puls;

public class Life {
    double Ves;
    int Step;

    public Life(double ves, int step) {
        Ves = ves;
        Step = step;
    }

    public double getVes() {
        return Ves;
    }

    public void setVes(double ves) {
        Ves = ves;
    }

    public int getStep() {
        return Step;
    }

    public void setStep(int step) {
        Step = step;
    }
}
